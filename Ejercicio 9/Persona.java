
public class Persona {
	int edad;
	String Nombre;
	String Telefono;
	Persona(int edad,String nombre, String telefono){
		this.edad=edad;
		this.Nombre=nombre;
		this.Telefono=telefono;
	}
	public int getEdad() {
		return edad;
	}
	public String getNombre() {
		return Nombre;
	}
	public String getTelefono() {
		return Telefono;
	}
	
	
}
